void download_begin() {
  WiFi.begin("DhonanAP", "epiepiepi");
  while(WiFi.status() != WL_CONNECTED);
  Serial.println("Connected to DhonanAP");

  if(SD.begin(16)) {
    Serial.println("SD card ready");
  }
  
  // Download and save the file
  WiFiClientSecure *client = new WiFiClientSecure;
  if(client) {
    client -> setCACert(rootCACertificate);
  }
  HTTPClient https;

  if (https.begin(*client, "https://github.com/Capstone-A14/cardimetry-mcu-dlc/raw/master/fonts/PoppinsLight10.vlw")) {
    int httpCode = https.GET();
    Serial.println(httpCode);

    if (httpCode == HTTP_CODE_OK) {
      File file = SD.open("test.vlw", FILE_WRITE);

      if (file) {
        WiFiClient* stream = https.getStreamPtr();
        while (stream->connected()) {
          String line = stream->readStringUntil('\n');
          file.println(line);
        }
        file.close();
        Serial.println("File downloaded and saved successfully.");
      } else {
        Serial.println("Error opening file for writing.");
      }
    } else {
      Serial.println("HTTPS request failed.");
    }

    https.end();
  }
}

